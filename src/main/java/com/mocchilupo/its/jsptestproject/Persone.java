package com.mocchilupo.its.jsptestproject;


import com.mocchilupo.its.jsptestproject.util.DataTransferObject;

public class Persone implements DataTransferObject {
    private long id;
    private String nome;
    private String cognome;
    private String email;
    private String data_di_nascita;
    private String telefono;

    public Persone(String nome, String cognome, String email, String data_di_nascita, String telefono) {
        this.nome = nome;
        this.cognome = cognome;
        this.email = email;
        this.data_di_nascita = data_di_nascita;
        this.telefono = telefono;
    }

    public long getId() {
        return id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCognome() {
        return cognome;
    }

    public void setCognome(String cognome) {
        this.cognome = cognome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getData_di_nascita() {
        return data_di_nascita;
    }

    public void setData_di_nascita(String data_di_nascita) {
        this.data_di_nascita = data_di_nascita;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }
}
